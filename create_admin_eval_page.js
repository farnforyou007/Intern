const fs = require('fs');
const path = require('path');
// Use Buffer to handle Thai characters properly
const base = Buffer.from('d:/สหกิจ/internship-system/src/app', 'utf8').toString();
const src = path.join(base, 'teacher', 'subjects', 'page.tsx');
const dstDir = path.join(base, 'admin', 'evaluations');
const dst = path.join(dstDir, 'page.tsx');
console.log('Reading from:', src);
console.log('Exists:', fs.existsSync(src));
let content = fs.readFileSync(src, 'utf8');
content = content.replace("import liff from '@line/liff'\n", '');
content = content.replace("import { getLineUserId } from '@/utils/auth'\n", '');
content = content.replace("import Swal from 'sweetalert2'", "import Swal from 'sweetalert2'\nimport AdminLayout from '@/components/AdminLayout'");
content = content.replace('export default function EvaluationSummary()', 'export default function AdminEvaluationResults()');
content = content.replace(/const urlParams = new URLSearchParams\(window\.location\.search\);\s*\n\s*const lineUserId = await getLineUserId\(urlParams\);\s*\n\s*if \(!lineUserId\) \{\s*\n\s*setLoading\(false\)\s*\n\s*setIsTabLoading\(false\)\s*\n\s*return;\s*\n\s*\}\s*\n/, '');
content = content.replace(/\/api\/teacher\/subjects\?lineUserId=\$\{encodeURIComponent\(lineUserId\)\}\$\{subjectParam\}\$\{yearParam\}/, '/api/admin/evaluations?${subjectParam.replace(\"&\",\"\")}\${yearParam}');
content = content.replace("const res = await fetch('/api/teacher/subjects',", "const res = await fetch('/api/admin/evaluations',");
content = content.replace('ผลการประเมินรายวิชาที่รับผิดชอบ — Weighted Summary', 'ผลการประเมินรายวิชาทั้งหมด — Admin Overview');
fs.mkdirSync(dstDir, { recursive: true });
fs.writeFileSync(dst, content, 'utf8');
console.log('SUCCESS: ' + fs.statSync(dst).size + ' bytes');
